package operators;

public class ArithematicOperatorsNmbrsEx {
	public static void main(String[] args) {
		int a = 9, b = 4, add, sub, mul, div, mod;
		add = a + b;
		sub = a - b;
		mul = a * b;
		div = a / b;
		mod = a % b;
		System.out.println("Addition Result:" + " " + add);
		System.out.println("Substraction Result:" + " " + sub);
		System.out.println("Multiplication Result:" + " " + mul);
		System.out.println("Division Result:" + " " + div);
		System.out.println("Modular Division Result:" + " " + mod);
	}
}