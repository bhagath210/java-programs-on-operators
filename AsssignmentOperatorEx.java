package operators;

public class AsssignmentOperatorEx {
	public static void main(String[] args) {
		int z;
		boolean bl;
		String Name;
		z = 18;
		bl = true;
		Name = "Virat Kohli";
		System.out.printf("By using assignment operator(=) the different types of variables are assigned as:\n");
		System.out.println("No." + z + " " + "is" + " " + Name + " " + bl);
	}
}